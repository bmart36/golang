package main

import "fmt"

func main() {
	// Using shor declaration operator, ASSIGN these VALUES to VARIABLES
	// with the IDENTIGIERS "x" and "y" and "z"
	//     a. 42
	//     b. "James Bond"
	//     c. true
	x := 42
	y := "James Bond"
	z := true

	// Print values using a single print statement
	fmt.Println(x, y, z)

	// Print values using multiple print statments
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}